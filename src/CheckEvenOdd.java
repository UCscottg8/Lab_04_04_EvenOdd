public class CheckEvenOdd {
    public static void main(String[] args) {
        int numToExamine = 5;

        if (numToExamine % 2 == 0) {
            System.out.println(numToExamine + " is an even number.");
        } else {

            // Output statement
            System.out.println(numToExamine + " is an odd number.");
        }
    }
}
